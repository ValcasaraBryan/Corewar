#include <libft.h>

void	ft_putnbr(int n)
{
	int limite;

	limite = 0;
	if (n == -2147483648)
	{
		limite = 1;
		n = n + 1;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n / 10 != 0)
		ft_putnbr(n / 10);
	ft_putchar(limite == 1 ? (n % 10) + 1 + '0' : n % 10 + '0');
}
